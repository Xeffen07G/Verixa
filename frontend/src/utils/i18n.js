/**
 * Translation function
 * Falls back to English if key not found in selected language
 */
export function t(key, lang = 'en') {
  const normalizedLang = (lang || 'en').toLowerCase();
  const dict = translations[normalizedLang] || translations.en;
  return dict[key] || translations.en[key] || key;
}

/**
 * Get/set preferred language from localStorage
 */
export function getStoredLanguage() {
  return localStorage.getItem('verixa-lang') || 'en';
}

export function setStoredLanguage(lang) {
  localStorage.setItem('verixa-lang', lang.toLowerCase());
}
